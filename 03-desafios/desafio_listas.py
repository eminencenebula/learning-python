# Desafio: Trabalhando com listas
# Dada uma lista de números, exiba:
# - o maior número
# - o menor número
# - a soma de todos os valores
# - a média dos valores

numeros = [4, 7, 1, 9, 3, 10, 2]

print("Lista:", numeros)
print("Maior número:", max(numeros))
print("Menor número:", min(numeros))
print("Soma:", sum(numeros))
print("Média:", sum(numeros) / len(numeros))
